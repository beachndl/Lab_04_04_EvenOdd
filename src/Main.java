public class Main {
    public static void main(String[] args) {
        // Number to examine
        int numToExamine = 2;

        // Calculate the modulo result
        int moduloResult = numToExamine % 2;

        // Output the results
        System.out.println("Modulo result of " + numToExamine + " is " + moduloResult);
        System.out.println("If the result of mod is 0, it is even.");
        System.out.println("If the result of mod is 1, it is odd.");
    }
}